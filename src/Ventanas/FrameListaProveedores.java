    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
        Date date = new Date();
        Calendario.setDate(date);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        String Fecha = sdf.format(Calendario.getDate());
        //Llamada al metodo BD
        MetodosDB metodosbd = new MetodosDB();
        //aqui llamamos al metodo para hacer el registro en la base de datos
        double Horas, Costo, Saldo;
        Horas = Double.parseDouble(txtHoras.getText());
        Costo = Horas * 130;
        Saldo = Double.parseDouble(txtSaldo.getText());
        String Nombre = txtNombre.getText();
        //Poniendo el formato al calendario
        int registro;
        registro = metodosbd.Guardar(
                Nombre,
                txtApellido.getText(),
                cboxMateria.getSelectedItem().toString(),
                Horas,
                Costo,
                Double.parseDouble(txtMonto.getText()),
                Saldo,
                Fecha
        );
        if (Nombre.equals("")) {
            JOptionPane.showMessageDialog(null, "ingresar un nombre");
        }
        if (registro > 0) {

            JOptionPane.showMessageDialog(null, "Los datos se han guardado correctamente",
                    "Éxito en la operación", JOptionPane.INFORMATION_MESSAGE);

        } else {

            JOptionPane.showMessageDialog(null, "Los datos no se pudieron guardar\n"
                    + "Inténtelo nuevamente", "Error en la operación", JOptionPane.ERROR_MESSAGE);

        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Revisa la información");
        }


    }//GEN-LAST:event_btnRegistrarActionPerformed

