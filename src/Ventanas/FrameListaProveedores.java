    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int dialog = JOptionPane.showConfirmDialog(null, "¿Seguro que desea eliminar el registro?", "Eliminar registro", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (dialog == JOptionPane.YES_OPTION) {
            try {
                int row = tblDatos.getSelectedRow();
                DefaultTableModel model = (DefaultTableModel) tblDatos.getModel();
                String selected = model.getValueAt(row, 0).toString();

                if (row >= 0) {

                    model.removeRow(row);

                    try {
                        Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "root");
                        PreparedStatement ps = conn.prepareStatement("delete from cuenta where idCuentas='" + selected + "' ");
                        ps.executeUpdate();
                    } catch (SQLException w) {
                        JOptionPane.showMessageDialog(null, "Error de conexion");
                    }
                }
            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(null, "Selecciona algún registro para eliminar");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double Horas, Costo, Monto, Saldo;
        Horas = Double.parseDouble(txtHoras.getText());
        Costo = Horas * 130;
        Monto = Double.parseDouble(txtMonto.getText());
        Saldo = Costo - Monto;
        txtSaldo.setText("" + Saldo);
        txtCosto.setText("" + Costo);
        if (Horas == 0) {
            JOptionPane.showMessageDialog(null, "Ingresar numero de horas, favor de solo ingresar numeros");
        }
        if (Monto == 0) {
            JOptionPane.showMessageDialog(null, "Ingresar un monto");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

